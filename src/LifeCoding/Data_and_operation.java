package LifeCoding;

public class Data_and_operation {
    public static void main(String [] args) {
        System.out.println(6); // 숫자를 표현 할 때
        System.out.println("Six"); // String "문자" 정확하게는 문자열
        System.out.println("6"); // String 문자 6

        System.out.println(6+6); // 12
        System.out.println("6"+"6"); // 66

        System.out.println(6*6); // 36
//      System.out.println("6"*"6");

        System.out.println("1111".length()); // 4 문자열의 길이를 알려줌
        System.out.println(1111); // 1111
//      System.out.println(1111.length());

        System.out.println("Hello World"); // String 문자열
        System.out.println('H'); // Char 문자
        System.out.println("H");

    }
}
